package fr.diginamic.jdr;

public class Personnage {

	protected int force;
	protected int pointDeVie;

	public Personnage() {

	}

	public int getForce() {
		return force;
	}

	public void setForce(int force) {
		this.force = force;
	}

	public int getPointDeVie() {
		return pointDeVie;
	}

	public void setPointDeVie(int pointDeVie) {
		this.pointDeVie = pointDeVie;
	}

}
